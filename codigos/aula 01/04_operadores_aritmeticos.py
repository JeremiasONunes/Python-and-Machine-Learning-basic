# Aula 01 - Tópico 3: Operadores e Cálculos
# Demonstração de operadores aritméticos e lógicos

print("=== OPERADORES ARITMÉTICOS ===")

# Números para demonstração
a = 15
b = 4

print(f"a = {a}, b = {b}")
print(f"Adição: {a} + {b} = {a + b}")
print(f"Subtração: {a} - {b} = {a - b}")
print(f"Multiplicação: {a} * {b} = {a * b}")
print(f"Divisão: {a} / {b} = {a / b}")
print(f"Divisão inteira: {a} // {b} = {a // b}")
print(f"Resto da divisão: {a} % {b} = {a % b}")
print(f"Potenciação: {a} ** {b} = {a ** b}")

print("\n=== OPERADORES LÓGICOS ===")

# Variáveis booleanas
x = True
y = False

print(f"x = {x}, y = {y}")
print(f"x and y = {x and y}")
print(f"x or y = {x or y}")
print(f"not x = {not x}")
print(f"not y = {not y}")

print("\n=== OPERADORES DE COMPARAÇÃO ===")
print(f"{a} > {b} = {a > b}")
print(f"{a} < {b} = {a < b}")
print(f"{a} == {b} = {a == b}")
print(f"{a} != {b} = {a != b}")
print(f"{a} >= {b} = {a >= b}")
print(f"{a} <= {b} = {a <= b}")